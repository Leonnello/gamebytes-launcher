    private void submitButtonMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_submitButtonMouseClicked
        submitPanel.setBackground(new Color(255, 102, 51));
        String username = usernameBox.getText();
        char[] pword = pwordBox.getPassword();
        Account account;
        //missing input error
        if(username.isEmpty() || pword.length == 0){
            JOptionPane.showMessageDialog(rootPane, "Must fill-in all text boxes.");
            return;
        }
        
        if(signupSelected){
            //duplicate username
            try{
                account = new Account(username, pword);
            } catch (Exception e) {
                JOptionPane.showMessageDialog(rootPane, "Username was already taken.");
                return;
            }
            accounts.add(account);
            JOptionPane.showMessageDialog(rootPane, "Account has been successfully added.");
            return;
        }
        
        //search for matching username in accounts then check if it is paired with the correct password
        if(loginSelected){
            for(Account acc: accounts){
                if(acc.getUsername().equals(username))
                    if(Arrays.equals(acc.getPassword(), pword)){
                        //switch frames
                        Save();//save accountData
                        //this.setVisible(false);
                        
                        /* Create and display the form */
                        java.awt.EventQueue.invokeLater(new Runnable() {
                            public void run() {

                                mainLauncher.updateLauncherState(true, acc);
//                                if(!viewOnce){
                                // mainFrame = new MainFrame(loginFrame, acc);
                                //     mainFrame.setVisible(true);
                                    
  //                                  viewOnce = true;
    //                            } else
      //                              mainFrame.
        //                            mainFrame.setVisible(true);
                            }
                        });
                        this.dispose();
                        return;
                    }
                    else{
                        JOptionPane.showMessageDialog(rootPane, "Incorrect password.");
                        return;
                    }
            }
            JOptionPane.showMessageDialog(rootPane, "Username does not exist.");
            return;
        }
        JOptionPane.showMessageDialog(rootPane, "Select sign up or log in first.");
    }//GEN-LAST:event_submitButtonMouseClicked

